from flask_restful import reqparse

post_parser = reqparse.RequestParser()
post_parser.add_argument('job', required=True)
post_parser.add_argument('team_leader', required=True, type=int)
post_parser.add_argument('work_size', required=True, type=int)
post_parser.add_argument('collaborators', required=False)
post_parser.add_argument('start_date', required=False)
post_parser.add_argument('end_date', required=False)
post_parser.add_argument('category', required=True, type=int)
post_parser.add_argument('is_finished', required=False, type=int)

